	<script>
	$(document).ready(function() {

		//triggers basket icon (button), then redirects to shopping cart
		$(".gotoshopcart").click(function(){
			window.location.href = '/shop-cart';
		});

		//tester for <input id="qty"> and <p id="qty_display"/>
		$("#qty").keyup(function() {
			var value = $(this).val();
			$("#qty_display").text(value);
		}).keyup();

		//update cart quantity
		$('.update-cart').click(function() {
			console.log($(this).attr('data-cart'));
			console.log($("#qty_"+$(this).attr('data-cart')).val());
			var qty = $("#qty_"+$(this).attr('data-cart')).val();
			console.log(qty);

			$.ajax({
				type: "post",
				dataType: 'json',
				data: {
					rowId: $(this).attr('data-cart'),
					qty: qty,
					_token: $('meta[name="csrf-token"]').attr('content')
				},
				cache: false,
				url: '/cart-update',
				success: function(data){
					console.log(data);
					
					$.notify({
						// options
						message: 'Your cart has been successfully updated.'
					},{
						// settings
						type: 'success',
						delay: 1000,
						offset : {
							y: 150,
							x: 20
						}
					});

					//reload page after success notification
					setTimeout(function(){
					    location.reload('/shop-cart');
					}, 2800);
				},
				error: function(){  // error handling
					console.log();
				}
			});
		});
		// console.log(dataArray);
	});
	</script>

